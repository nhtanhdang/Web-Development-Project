  <h2>
    What is Streaming?
  </h2>
</div>

<br>
<br>

<div class="text-container">
<p>Streaming overall is a system or method of showing video or listening audio content alternative of downloading the client video files.
</p>
</div>
</div>

        <ul>
        <li>
            <section class="bg-image-2">
              <h2>
                <strong>Purpose</strong>
              </h2>
            </section>
             <div class="text-container">
              Consumers now can watch TV programs of all genres without the need of downloading files which take up storage space on their devices. Most of streaming platforms charge users on a recurring basis (user subscription model). The amount of money seems reasonable for users, taking into consideration offers of these platforms, such as high-definition (HD) or even 4K Ultra HD streaming, original content, no ads and many more. Streaming service also enhances interaction between users with live chat where people can communicate with each other simultaneously. 
              <br><br>
              Moreover, anyone can be a “streamer” with only one internet-connected device. They can stream from their own mobile phones or laptops via numerous streaming platforms such as Youtube, Facebook, TikTok, etc. This has contributed greatly to the diversity of entertaining content that we are now consuming on a daily basis.               
             </div>
        </li> 
        
        <li>
            <section class="bg-image-3">
              <h2>
                <strong>Function</strong>
              </h2>
            </section>
             <div class="text-container-1">
              Audio or video files are broken into small data packets and every packet contains a small bunch of data<a class="footer-style" href="#section3" id="footnote4">[4]</a>. Either TCP (Transmission Control Protocol) or UDP (User Datagram Protocol) is used to exchange data over a network. When a client requests to receive the data packets, a video player will decompress the data and interpret video and audio. The more usable one for streaming is TCP as it’s more reliable protocol while UDP is more suitable for video conferencing. Video streaming is basically pre-recorded video files or a live broadcast feed. In terms of live broadcast, the video signal is converted into compressed digital signal from a server and can be sent to multiple users at the same time. 
             </div>
        </li> 

        <li>
            <section class="bg-image-4">
              <h2>
                <strong>Feature</strong>
              </h2>
            </section>
             <div class="text-container">
              Different features exist on streaming video (E.g., 720p, 1080i, 1080p, 4K). The more video quality, the higher speed needed for streaming and receiving higher smooth video quality.  Usually, most programs are broadcast at a standard of 720p and on the other hand, Netflix, Amazon prime video offer 1080p, 2160p or 4K.
