    <div class="topic">
      There are many reasons why streaming media’s popularity is growing exponentially. However, the key factor contributing to this rapid growth is its convenience. This is considered as the opening of a new era since TV programs’ consumers all over the world then were exhausted of being tied to a TV schedule. Subscribers of streaming service can choose when they want to watch and what they want to see. The schedule now depends on them.
      <br><br>
      In the future, there is a great chance that people will be able to solve two major problems of streaming service, which are speed and quality. Consumers now are still having trouble with watching stream videos during peak hours because streaming takes up a lot of data and speed. Furthermore, there are a growing number of algorithms developed to personalise the users’ experience. The platform can now give recommendations on next movies based on users’ choices of previous movies. The accuracy of these recommendations will become considerably high soon. To summarise, this technology has a great potential of becoming a major field in the industry.         
