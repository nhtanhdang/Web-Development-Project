      <div class="topic">
        <dl>
          <dt><strong>iTV</strong></dt>
          <dd>The first streaming service - video-on-demand service is iTV (interactive TV), launched by Hong Kong Telecom (HKT) in 1998. 
          </dd>
        </dl>
        <br>
        In 1993, when cable television first came to Hong Kong, there was a huge surge in its subscribers that soon made up 25 percent of the market. Following this trend, HKT was about to launch a video-on-demand service, which required more than $1,5 billion. At that time, Hong Kong had one of the highest per-capita incomes in the world and the region was small enough to be covered with fiber-optic cable. HKT also conducted a research showing around 80 percent of all the subscribers are fascinated by the iTV. All these factors had made Hong Kong the promised land for streaming service. 
            
        The launch, however, turned out to be not very successful due to many reasons. Only after PCCW acquired HKT had it really blown up.   
